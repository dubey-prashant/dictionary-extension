  // Get local suggestions matching the query
  getSuggestions(query, maxResults = 6) {
    if (!query || query.length < 1) return [];

    const searchTerm = query.toLowerCase();
    const suggestions = [];

    // First, find words that start with the query (highest priority)
    for (const word of this.words) {
      if (word.startsWith(searchTerm)) {
        suggestions.push(word);
        if (suggestions.length >= maxResults) break;
      }
    }

    // If we don't have enough suggestions, find words that contain the query
    if (suggestions.length < maxResults) {
      for (const word of this.words) {
        if (!word.startsWith(searchTerm) && word.includes(searchTerm)) {
          suggestions.push(word);
          if (suggestions.length >= maxResults) break;
        }
      }
    }

    return suggestions;
  }

  // Check if a word exists in our local list
  hasWord(word) {
    return this.words.includes(word.toLowerCase());
  }

  // Get word count
  getWordCount() {
    return this.words.length;
  }
